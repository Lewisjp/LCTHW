#include <stdio.h>

int main()
{
    

    int age = 10;
    int height = 72;

    printf("I am %d years old.\n", age);
    printf("I am %d inches tall.\n", height);

    char cat = 'c';
    float waist = 32.6;
    
    printf("The word Cat begins with %c.\n", cat);
    printf("My waist is %f", waist);
    
    return 0;
}

